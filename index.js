$("section").on("mouseenter", function () {
	$(this).toggleClass("section-hover")
})
$("section").on("mouseleave", function () {
	$(this).toggleClass("section-hover")
})

$(".first-link").on("mouseenter", function(){
	$(this).toggleClass("link")
})
$(".first-link").on("mouseleave", function(){
	$(this).toggleClass("link")
})